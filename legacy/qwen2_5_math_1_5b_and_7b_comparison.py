"""The system prompt is taken from the official page of the models on HF."""

SYSTEM_PROMPT = "Please reason step by step, and put your final answer within \\boxed{}."

number_re = re.compile(r"-?\d+(?:,\d{3})*(?:\.\d+)?")
boxed_re = re.compile(r"\\boxed\{([^}]+)\}", re.S)

def canonicalize_answer(text):
    s = str(text).strip().replace(",", "").replace("$", "")
    s = s.replace(" ", "")
    if s.endswith(".0"):
        s = s[:-2]
    if s.endswith("."):
        s = s[:-1]
    return s

def extract_gold_answer(answer_value):
    return canonicalize_answer(answer_value)

def extract_pred_answer(text):
    matches = boxed_re.findall(text)
    if matches:
        return canonicalize_answer(matches[-1].strip())
    nums = number_re.findall(text.replace(",", ""))
    if nums:
        return canonicalize_answer(nums[-1])
    return canonicalize_answer(text)

def build_prompt(tokenizer, prompt):
    messages = [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": prompt.strip()},
    ]
    return tokenizer.apply_chat_template(messages, tokenize=False, add_generation_prompt=True)

def load_model_and_tokenizer(model_id):
    tokenizer = AutoTokenizer.from_pretrained(model_id, trust_remote_code=True)
    tokenizer.padding_side = "left"
    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token

    model = AutoModelForCausalLM.from_pretrained(
        model_id,
        device_map="auto",
        torch_dtype=torch.bfloat16 if bf16 else torch.float16,
        trust_remote_code=True,
        low_cpu_mem_usage=True,
        attn_implementation="sdpa",
    )
    model.generation_config.pad_token_id = tokenizer.pad_token_id
    model.eval()
    return model, tokenizer

def generate_batch(model, tokenizer, prompts, max_new_tokens=MAX_TOKES):
    device = next(model.parameters()).device
    inputs = tokenizer(prompts, return_tensors="pt", padding=True, truncation=True).to(device)
    prompt_len = inputs["input_ids"].shape[1]
    with torch.inference_mode():
        outputs = model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            do_sample=False,
            eos_token_id=tokenizer.eos_token_id,
            pad_token_id=tokenizer.pad_token_id,
        )
    decoded = []
    for i in range(outputs.size(0)):
        text = tokenizer.decode(outputs[i][prompt_len:], skip_special_tokens=False)
        decoded.append(text)
    return decoded

def evaluate_exact_match(model, tokenizer, dataset, batch_size=4, max_new_tokens=MAX_TOKES):
    rows = []
    correct = 0
    for start in tqdm(range(0, len(dataset), batch_size)):
        batch = dataset.select(range(start, min(start + batch_size, len(dataset))))
        prompts = [build_prompt(tokenizer, x) for x in batch["prompt"]]
        decoded = generate_batch(model, tokenizer, prompts, max_new_tokens=max_new_tokens)
        for prompt_text, gold_answer_value, pred_text, steps in zip(batch["prompt"], batch["answer"], decoded, batch["steps"]):
            pred = extract_pred_answer(pred_text)
            gold = extract_gold_answer(gold_answer_value)
            is_correct = int(pred == gold)
            correct += is_correct
            rows.append({
                "prompt": prompt_text,
                "gold_answer": gold,
                "pred_answer": pred,
                "correct": is_correct,
                "pred_text": pred_text,
                "steps": int(steps),
            })
    df = pd.DataFrame(rows)
    return df, correct / len(dataset)

