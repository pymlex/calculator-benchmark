from scipy.stats import chi2
import pandas as pd

model_a = "Qwen/Qwen2.5-Math-7B-Instruct"
model_b = "Qwen/Qwen2.5-Math-1.5B-Instruct"

df_a = combined[combined["model_id"] == model_a][["prompt", "correct"]].copy()
df_b = combined[combined["model_id"] == model_b][["prompt", "correct"]].copy()

paired = df_a.merge(df_b, on="prompt", suffixes=("_a", "_b"))

contingency = pd.crosstab(paired["correct_a"], paired["correct_b"])

b = int(contingency.loc[1, 0]) if (1 in contingency.index and 0 in contingency.columns) else 0
c = int(contingency.loc[0, 1]) if (0 in contingency.index and 1 in contingency.columns) else 0

stat = (abs(b - c) - 1) ** 2 / (b + c) if (b + c) > 0 else 0.0
p_value = 1.0 - chi2.cdf(stat, 1)

print(f"\nMcNemar statistic (with Yates correction): {stat:.4f}")
print(f"p-value: {p_value}")
if p_value < 0.05:
    print("The difference is statistically significant at the 0.05 level.")
else:
    print("No statistically significant difference detected (p >= 0.05).")

